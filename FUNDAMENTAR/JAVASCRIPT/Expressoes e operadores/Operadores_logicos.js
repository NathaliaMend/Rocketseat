// Operadores logicos ( logical operators)

// - 2 valores booleanos, quando verificados, resultara em verdadeiro ou falso

let pao = true
let queijo = true

// AND &&  ( o console.log imprimira true somente se todos os atributos forem verdadeiros/true)( o console.log imprime false quando um ou mais atributos tiverem valor false )
console.log(pao && queijo)

// OR || (qualquer um que for verdadeiro, o console.log imprime true)( o console.log imprimira false somente se todos os atributos forem falsos/false)
console.log(pao || queijo)

// NOT ! (Negar/Negacao)(o simbolo de negacao serve para trocar o valor do booleano)(ele fica semanticamente antes do atributo)
console.log(!pao)
 